package com.example.myapplication;

public class Reject_details {
    String username, phonenum, email, vehicleno, problem,lattitude, longitude,emailId;

    public Reject_details(String username, String phonenum, String email, String vehicleno, String problem, String lattitude, String longitude, String emailId) {
        this.username = username;
        this.phonenum = phonenum;
        this.email = email;
        this.vehicleno = vehicleno;
        this.problem = problem;
        this.lattitude = lattitude;
        this.longitude = longitude;
        this.emailId = emailId;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPhonenum() {
        return phonenum;
    }

    public void setPhonenum(String phonenum) {
        this.phonenum = phonenum;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getProblem() {
        return problem;
    }

    public void setProblem(String problem) {
        this.problem = problem;
    }

    public String getVehicleno() {
        return vehicleno;
    }

    public void setVehicleno(String vehicleno) {
        this.vehicleno = vehicleno;
    }

    public String getLattitude() {
        return lattitude;
    }

    public void setLattitude(String lattitude) {
        this.lattitude = lattitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getEmailId() {
        return emailId;
    }

    public void setEmailId(String emailId) {
        this.emailId = emailId;
    }
}
